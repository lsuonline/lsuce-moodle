defined('MOODLE_INTERNAL') || die();

function xmldb_biosigid_upgrade($oldversion) {
    global $CFG, $DB;

    $dbman = $DB->get_manager();

    if ($oldversion < 2016083000) {
        // Add new fields to certificate table.
        $table = new xmldb_table('biosigid');
        $field = new xmldb_field('attempts');
        $field->set_attributes(XMLDB_TYPE_INTEGER, '1', null, XMLDB_NOTNULL, null, '0');
        if (!$dbman->field_exists($table, $field)) {
            $dbman->add_field($table, $field);
        }

        // Savepoint reached.
        upgrade_mod_savepoint(true, 2016083000, 'biosigid');
    }
    return true;
}